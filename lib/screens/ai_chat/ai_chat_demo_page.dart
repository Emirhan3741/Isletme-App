                        const SizedBox(width: 8),
                        Expanded(
                          child: Text(
                            '${entry.value}',
                            style: TextStyle(color: Colors.grey[600]),
                          ),
                        ),
                      ],
                    ),
                  );
                }).toList(),
              ),
            ),
          ],
        );
      },
    );
  }

  /// ‚öôÔ∏è Demo ayarlarƒ±
  void _showDemoSettings() {
    showDialog(
